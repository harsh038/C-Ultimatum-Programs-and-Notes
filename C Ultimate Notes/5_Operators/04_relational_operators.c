// Relational Operators

// ==, !=, >, <, >=, <=
#include <stdio.h>

int main()
{
    int a = 10, b = 20;
    printf("Equal: %d\n", a == b);
    printf("Not Equal: %d\n", a != b);
    printf("Greater than: %d\n", a > b);
    printf("Less than: %d\n", a < b);
    printf("Greater than or equal: %d\n", a >= b);
    printf("Less than or equal: %d\n", a <= b);

    return 0;
}