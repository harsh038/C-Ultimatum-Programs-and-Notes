// Keywords

// Keywords are reserved words with special meanings.
// Examples: int, float, if, else, return

#include <stdio.h>

// Example program: Uses keywords
int main()
{
    int num = 5; // 'int' is a keyword
    if (num > 0)
    { // 'if' is a keyword
        printf("Number is positive\n");
    }
    return 0; // 'return' is a keyword
}