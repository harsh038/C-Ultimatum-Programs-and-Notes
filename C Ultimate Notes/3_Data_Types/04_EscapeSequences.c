// Escape Sequences

// Escape sequences represent special characters.
#include <stdio.h>

int main()
{
    printf("Newline:\n");          // \n - Newline
    printf("Tab:\tTab\n");         // \t - Tab
    printf("Backslash: \\ \n");    // \\ - Backslash
    printf("Double quote: \" \n"); // \" - Double quote

    return 0;
}